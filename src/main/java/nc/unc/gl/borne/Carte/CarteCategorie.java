package nc.unc.gl.borne.Carte;

/**
 * La catégorie d'une carte
 * BORNE Permet d'avancer
 * ATTAQUE Inflige un malus à un adversaire
 * PARADE Contre l'effet d'une carte ATTAQUE
 * BOTTE L'immunité contre une carte ATTAQUE
 */
public enum CarteCategorie {
    BORNE,
    ATTAQUE,
    PARADE,
    BOTTE;
}
